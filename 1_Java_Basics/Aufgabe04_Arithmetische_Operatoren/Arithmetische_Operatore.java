public class Arithmetische_Operatoren {
    public static void main(String[] args) {
        int y = 13;
        int x = 14;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
    }
}
